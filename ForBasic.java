package ch05;

public class ForBasic {

	public static void main(String[] args) {
		for (int i = 0; i < 5; i++)
			System.out.println("I love Java" +1);
		
		
		int sum = 1;
		for (int n = 1; n < 11; n++) {
			sum *= n;
			System.out.println(sum);
		}		

		

		for (int n1 = 1; n1<10 ;n1++) {
			System.out.println(n1*5);
		}
		
	}

}
